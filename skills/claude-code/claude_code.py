    def refactor(self, path: str, pattern: str, replacement: str, 
                 preview: bool = False) -> Dict:
        """
        批量重构
        
        Args:
            path: 重构路径
            pattern: 查找模式
            replacement: 替换内容
            preview: 是否预览
        """
        print(f"🔧 Claude Code: 批量重构...")
        print(f"   路径: {path}")
        print(f"   替换: '{pattern}' → '{replacement}'")
        
        target_path = Path(path)
        if not target_path.exists():
            return {"error": f"路径不存在: {path}"}
        
        py_files = list(target_path.rglob("*.py")) if target_path.is_dir() else [target_path]
        
        changes = []
        
        for f in py_files:
            content = f.read_text(encoding='utf-8')
            new_content = content.replace(pattern, replacement)
            
            if content != new_content:
                changes.append({
                    "file": str(f),
                    "replacements": content.count(pattern)
                })
                
                if not preview:
                    f.write_text(new_content, encoding='utf-8')
        
        action = "预览" if preview else "执行"
        print(f"✅ 重构{action}完成: {len(changes)} 个文件")
        
        return {
            "preview": preview,
            "changes": changes,
            "total_files": len(py_files)
        }
    
