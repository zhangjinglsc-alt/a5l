    def _execute_with_cli(self, task: str, **kwargs) -> Dict:
        """使用Claude Code CLI执行"""
        try:
            result = subprocess.run(
                ["claude", "-p", task],
                cwd=self.workspace,
                capture_output=True,
                text=True,
                timeout=300
            )
            
            return {
                "success": result.returncode == 0,
                "stdout": result.stdout,
                "stderr": result.stderr,
                "method": "cli"
            }
        except Exception as e:
            return {"error": str(e), "method": "cli"}
    
